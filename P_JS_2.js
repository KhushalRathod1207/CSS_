// Operators: An operator is a symbol that performs an operation on values or variables.

// 1. Arithmetic Operators

let a = 100;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// 2. Assignment Operators

console.log(a = b);
console.log(a += b);
console.log(a -= b);
console.log(a /= b);
console.log(a %= b);
console.log(a **= b);

// 3. Comparison Operators

console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);